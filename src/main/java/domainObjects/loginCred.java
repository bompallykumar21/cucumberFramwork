package domainObjects;

public class loginCred {

	private String username;
	private String password;
	private String userNameSecond;
	private String passwordSecond;
	
	public String getUsername() {
		return username;
	}
	public String getPassword() {
		return password;
	}
	public String getUserNameSecond() {
		return userNameSecond;
	}
	public String getPasswordSecond() {
		return passwordSecond;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public void setUserNameSecond(String userNameSecond) {
		this.userNameSecond = userNameSecond;
	}
	public void setPasswordSecond(String passwordSecond) {
		this.passwordSecond = passwordSecond;
	}
	
}

	
